# Что мы тестируем
# Мы тестируем базовые циклы и работу со строками.
# Это для новичков, которые только осваивают циклы и работу со строками.
#
# Отказ от ответственности
# Это для новичков, поэтому мы хотим протестировать базовые циклы и работу со строками.
# Продвинутые пользователи без труда справятся с этим в одну строку.


def smash(words):
    return " ".join(words)


print(smash(["hello", "world"]))
