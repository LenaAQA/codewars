# Напишите функцию, которая принимает массив слов, объединяет их в предложение и возвращает его.
# Вы можете игнорировать необходимость очистки слов или добавления знаков препинания,
# но пробелы между словами обязательны. Будьте внимательны: в начале и в конце предложения не должно быть пробелов!
#
# Пример
# ['hello', 'world', 'this', 'is', 'great']  =>  'hello world this is great'
# Предположения
# Вы можете предположить, что вам даны только слова.
